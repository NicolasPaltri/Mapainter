let oris = document.querySelector(".texto");
let o = document.querySelector(".circulo");

o.addEventListener('mouseover', () => {
    oris.style.opacity = '1';
   
})

o.addEventListener('mouseout', () => {
    oris.style.opacity = '0';

})
